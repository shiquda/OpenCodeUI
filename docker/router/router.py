def env_int(name, default):
    value = os.getenv(name, str(default))
    try:
        return int(value)
    except ValueError:
        return default


def parse_port_range(value):
    match = re.match(r"^(\d+)-(\d+)$", value or "")
    if not match:
        return (3000, 9999)
    start = int(match.group(1))
    end = int(match.group(2))
    if start > end:
        start, end = end, start
    return (start, end)


def parse_exclude_ports(value):
    ports = set()
    for item in (value or "").split(","):
        item = item.strip()
        if not item:
            continue
        try:
            ports.add(int(item))
        except ValueError:
            continue
    return ports


