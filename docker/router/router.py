def load_state():
    if not os.path.exists(ROUTER_STATE_FILE):
        return {}
    try:
        with open(ROUTER_STATE_FILE, "r", encoding="utf-8") as handle:
            return json.load(handle)
    except (OSError, json.JSONDecodeError):
        return {}


def save_state(state):
    os.makedirs(os.path.dirname(ROUTER_STATE_FILE), exist_ok=True)
    with open(ROUTER_STATE_FILE, "w", encoding="utf-8") as handle:
        json.dump(state, handle, ensure_ascii=True, indent=2, sort_keys=True)


def generate_token(length):
    alphabet = string.ascii_letters + string.digits
    return "".join(secrets.choice(alphabet) for _ in range(length))


def run_cmd(args):
    result = subprocess.run(
        args, stdout=subprocess.PIPE, stderr=subprocess.PIPE, text=True
    )
    if result.returncode != 0:
        log.warning(
            "Command failed (rc=%d): %s | stderr: %s",
            result.returncode,
            " ".join(args),
            result.stderr.strip(),
        )
        return None
    return result.stdout


def parse_proc_net_tcp(output):
    ports = set()
    for line in output.splitlines():
        line = line.strip()
        if not line or line.startswith("sl"):
            continue
        parts = line.split()
        if len(parts) < 4:
            continue
        local = parts[1]
        state = parts[3]
        if state != "0A":
            continue
        if ":" not in local:
            continue
        port_hex = local.rsplit(":", 1)[-1]
        try:
            port = int(port_hex, 16)
        except ValueError:
            continue
        ports.add(port)
    return ports


def list_listening_ports():
    output = run_cmd(
        [
            "docker",
            "exec",
            TARGET_CONTAINER,
            "sh",
            "-c",
            "cat /proc/net/tcp /proc/net/tcp6",
        ]
    )
    if output is None:
        log.warning("Failed to query ports from %s", TARGET_CONTAINER)
        return None
    ports = parse_proc_net_tcp(output)
    filtered = []
    start, end = PORT_RANGE
    for port in ports:
        if port in EXCLUDE_PORTS:
            continue
        if port < start or port > end:
            continue
        filtered.append(port)
    log.debug("Discovered ports: %s", filtered)
    return sorted(filtered)


def write_map(state):
    lines = ["# Dynamic routes (auto generated by router)"]
    for token, info in sorted(state.items()):
        if token.startswith("__"):
            continue
        port = info.get("port")
        if not port:
            continue
        lines.append(f"@route_{token} path /p/{token} /p/{token}/*")
        lines.append(f"handle @route_{token} {{")
        lines.append(f"\turi strip_prefix /p/{token}")
        lines.append(f"\treverse_proxy {TARGET_CONTAINER}:{port}")
        lines.append("}")
        lines.append("")
    content = "\n".join(lines) + "\n"
    with open(ROUTES_FILE, "w", encoding="utf-8") as handle:
        handle.write(content)


def reload_gateway():
    run_cmd(["caddy", "reload", "--config", "/etc/caddy/Caddyfile"])


# ---- Preview port management ----

preview_port = None  # Currently active preview port


def load_preview_state():
    global preview_port
    state = load_state()
    preview_port = state.get("__preview_port__")
    if preview_port is not None:
        write_preview_conf(preview_port)
        log.info("Restored preview port: %s", preview_port)


def save_preview_port(port):
    global preview_port
    preview_port = port
    state = load_state()
    if port is None:
        state.pop("__preview_port__", None)
    else:
        state["__preview_port__"] = port
    save_state(state)


def write_preview_conf(port):
    if port:
        content = (
            "# Preview proxy (auto generated by router)\n"
            f"reverse_proxy {TARGET_CONTAINER}:{port}\n"
        )
    else:
        content = (
            "# Preview proxy (no port selected)\n"
            'respond "No preview port configured. '
            'Set one from the Routes panel." 503\n'
        )
    with open(PREVIEW_FILE, "w", encoding="utf-8") as handle:
        handle.write(content)


def set_preview_port(port):
    write_preview_conf(port)
    save_preview_port(port)
    reload_gateway()
    log.info("Preview port set to: %s", port)


def sync_routes():
    state = load_state()
    ports = list_listening_ports()

    if ports is None:
        # docker exec failed â€” skip this cycle, do NOT clear existing routes
        return

    # Filter out non-route keys (e.g. __preview_port__)
    route_state = {k: v for k, v in state.items() if not k.startswith("__")}
    existing_ports = {info.get("port") for info in route_state.values()}

    changed = False
    for port in ports:
        if port in existing_ports:
            continue
        token = generate_token(TOKEN_LENGTH)
        while token in state:
            token = generate_token(TOKEN_LENGTH)
        state[token] = {
            "port": port,
            "created_at": int(time.time()),
        }
        log.info("New route: port %d -> /p/%s", port, token)
        changed = True

    active_ports = set(ports)
    stale_tokens = [
        token
        for token, info in state.items()
        if not token.startswith("__") and info.get("port") not in active_ports
    ]
    for token in stale_tokens:
        port = state[token].get("port")
        del state[token]
        log.info("Removed stale route: port %s (token %s)", port, token)
        changed = True

    if changed:
        write_map(state)
        save_state(state)
        reload_gateway()
        log.info(
            "Routes updated: %d active",
            len(state) - sum(1 for k in state if k.startswith("__")),
        )


def check_basic_auth(auth_header):
    if not ROUTER_PASSWORD:
        return True
    if not auth_header or not auth_header.lower().startswith("basic "):
        return False
    raw = auth_header.split(" ", 1)[1].strip()
    try:
        decoded = base64.b64decode(raw).decode("utf-8")
    except (ValueError, UnicodeDecodeError):
        return False
    if ":" not in decoded:
        return False
    user, pwd = decoded.split(":", 1)
    return user == ROUTER_USERNAME and pwd == ROUTER_PASSWORD


@app.post("/preview/set")
def post_preview_set():
    if not check_basic_auth(request.headers.get("Authorization")):
        return Response(
            "Unauthorized", status=401, headers={"WWW-Authenticate": "Basic"}
        )
    data = request.get_json(silent=True) or {}
    port = data.get("port")
    if port is not None:
        try:
            port = int(port)
        except (ValueError, TypeError):
            return jsonify({"error": "invalid port"}), 400
    set_preview_port(port)
    return jsonify({"ok": True, "previewPort": port})


